enum TipoLei {
  leiOrdinaria,
  leiComplementar,
  decreto,
  resolucaoControladoriaGeral,
  portariasSubsecretariaServicosCompartilhados
}
/*
A lei ordinária é um ato normativo primário e contém, em regra, 
normas gerais e abstratas. Embora as leis sejam definidas, 
normalmente, pela generalidade e abstração ("lei material"), 
estas contêm, não raramente, normas singulares 
("lei formal" ou "ato normativo de efeitos concretos").
Exemplo de lei formal:
  Lei orçamentária anual (Constituição, art. 165, § 5o);
  Leis que autorizam a criação de empresas públicas, sociedades de economia
    mista, autarquias e fundações (Constituição, art. 37, XIX).
  O STF tem entendido que os atos normativos de efeitos concretos, por não 
    terem o conteúdo material de ato normativo, não se sujeitam ao controle 
    abstrato de constitucionalidade.
*/
